public class Knight {

    private int hitPoints;
    private int damage;
    private int defence;

    public Knight() {
        hitPoints = 2000;
        damage = 150;
        defence = 80;
    }

    public Knight(int hitPoints, int damage, int defence) {
        this.hitPoints = hitPoints;
        this.damage = damage;
        this.defence = defence;
    }



}
